void USART_Init(BYTE BR)
// BR: 0x0B - 38400, 0x07 - 57600 
{
  // USART initialization
  // Communication Parameters: 8 Data, 1 Stop, No Parity
  // USART Receiver: On
  // USART Transmitter: On
  // USART Mode: Asynchronous
  // USART Baud rate: BR
  UCSR0A=0x00;
  
  //sic
  //UCSR0B=0x98;
  UCSR0B=0xD8;
  
  UCSR0C=0x06;
  UBRR0H=0x00;
  UBRR0L=BR;
}

